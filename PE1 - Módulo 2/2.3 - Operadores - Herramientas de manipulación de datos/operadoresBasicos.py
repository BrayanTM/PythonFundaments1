# Operadores básicos en Python
#+ Suma
# Suma de enteros y flotantes

print(-4 + 4)
print(-4. + 8)

# - Resta

print(-4 - 4)
print(4. - 8)
print(-1.1)

# * Multiplicación

print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

# / División

print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

# // División entera
# La división entera devuelve el cociente sin el residuo.

print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)

# % Módulo o Residuo

print(14 % 4)

# ** Exponenciación

print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)