# Teorema de Pitágoras
# El teorema de Pitágoras establece que en un triángulo rectángulo, el cuadrado de la hipotenusa es igual a la suma de los cuadrados de los catetos.
# a^2 + b^2 = c^2

a = 3
b = 4
c = (a**2 + b**2)**0.5
print("La hipotenusa es:", c)  # Imprime el valor de la hipotenusa

print("El teorema de Pitágoras se cumple:", a**2 + b**2 == c**2)  # Verifica si se cumple el teorema