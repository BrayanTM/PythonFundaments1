# Operadores y sus enlaces
# El enlace de un operador determina el orden en que se computan las operaciones de los operadores con la misma prioridad, los cuales se encuentran dentro de una misma expresión.

print(9 % 6 % 2)

print(2 ** 2 ** 3)

# Lista de prioridades de operadores
# 1. **
# 2. +, - Unarios
# 3. *, /, //, %
# 4. +, - Binarios

print(2 * 3 % 5)

# Operadores y paréntesis
# Los paréntesis se utilizan para cambiar el orden de evaluación de las expresiones, permitiendo que las operaciones dentro de los paréntesis se realicen primero.

print((5 * ((25 % 13) + 100) / (2 * 13)) // 2)

