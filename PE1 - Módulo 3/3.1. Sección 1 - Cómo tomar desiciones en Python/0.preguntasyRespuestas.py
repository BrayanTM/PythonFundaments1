# Comparación: Operador de igualdad
"""

Pregunta: son dos valores iguales?

para hacer esta pregunta se utiliza el == (igual igual) operador.

- = es un operador de asignación, se usa para asignar un valor a una variable.
- == es un operador de comparación, se usa para comparar dos valores.

"""

#TODO Operadores
#TODO ------ Igualdad: El operador igual a (==) ------

var = 0  # Asignando 0 a var
print(var == 0)

var = 1  # Asignando 1 a var
print(var == 0)

#TODO ------ Desigualdad: El operador no es igual a (!=) ------

var = 0  # Asignando 0 a var
print(var != 0)

var = 1  # Asignando 1 a var
print(var != 0)

#TODO ------ Operadores de comparación: mayor que (>) ------
var = 0  # Asignando 0 a var
print(var > 0)

var = 1  # Asignando 1 a var
print(var > 0)

#TODO ------ Operadores de comparación: mayor o igual que (>=) ------
var = 0  # Asignando 0 a var
print(var >= 0)

var = 1  # Asignando 1 a var
print(var >= 0)

#TODO ------ Operadores de comparación: menor o igual que (<=) ------
var = 0  # Asignando 0 a var
print(var <= 0)

var = 1  # Asignando 1 a var
print(var <= 0)

