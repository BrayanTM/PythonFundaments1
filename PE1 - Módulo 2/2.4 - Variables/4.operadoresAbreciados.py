
#! Operadores Abreviados
# Se utilizan para realizar operaciones matemáticas de manera más concisa.

#? Ejemplo de uso de operadores abreviados
# Se necesita calcular una serie de valores sucesivos de la potencia de 2.

x = 2
sheep = 1

x *= 2  # Multiplica x por 2 y asigna el resultado a x
sheep += 1  # Incrementa el valor de sheep en 1

print("Valor de x:", x)  # Imprime el valor de x
print("Número de ovejas:", sheep)  # Imprime el número de ovejas


#? Ejemplos de operadores abreviados

# Expresión: i = i + 2 * j
i = 5
j = 3
i += 2 * j  # Suma 2 veces j a i y asigna el resultado a i

print("Valor de i:", i)  # Imprime el valor de i

# * Expresión: var = var / 2
var = 4
var /= 2 # Divide var por 2 y asigna el resultado a var

print("Valor de var:", var)  # Imprime el valor de var

# * Expresión: rem = rem % 10
rem = 25
rem %= 10 # Calcula el resto de rem dividido por 10 y asigna el resultado a rem

print("Valor de rem:", rem)  # Imprime el valor de rem

# * Expresión: j = j - (i + var + rem)
j = 3
j -= (i + var + rem)  # Resta la suma de i, var y rem a j y asigna el resultado a j

print("Valor de j:", j)  # Imprime el valor de j

# * Expresión: x = x ** 2
x = 2
x **= 2  # Eleva x al cuadrado y asigna el resultado a x

print("Valor de x:", x)  # Imprime el valor de x

