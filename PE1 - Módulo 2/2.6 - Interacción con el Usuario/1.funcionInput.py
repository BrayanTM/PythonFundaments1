# Función input()
# La función input() se utiliza para obtener datos del usuario.
# Permite que el programa pause su ejecución y espere a que el usuario ingrese información.

print("Dime lo que sea...")
anything = input()
print("Hmm...", anything, "... ¿en serio?")
# En este ejemplo, el programa solicita al usuario que ingrese cualquier cosa
# y luego imprime lo que el usuario ha ingresado, junto con un mensaje de confirmación.
# La función input() devuelve el valor ingresado como una cadena de texto (string).