# Comentarios en Python
# Los comentarios son líneas de código que no se ejecutan.
# Se utilizan para explicar el código y hacerlo más legible.
# Los comentarios se inician con el símbolo #.

# Por que, cuando y donde usar comentarios
# Se deben usar para aclarar partes del código que pueden ser confusas.
# También son útiles para documentar el propósito de funciones o bloques de código.
# Es recomendable comentar el código mientras se escribe, no al final.

# Ejemplo de comentario en una línea
# Este es un comentario que explica la siguiente línea de código
print("Hola, mundo!")  # Este es un comentario al final de una línea de código

# Ejemplo de comentario en varias líneas
"""
Este es un comentario de varias líneas.
Se utiliza para explicar bloques de código más grandes o para dejar notas importantes.
Los comentarios de varias líneas se encierran entre comillas triples.
"""
# Comentarios en Python
print("Este es un ejemplo de comentario en varias líneas")  # Este comentario es breve

# Comentarios dentro de funciones
def suma(a, b):
    # Esta función suma dos números y devuelve el resultado
    return a + b